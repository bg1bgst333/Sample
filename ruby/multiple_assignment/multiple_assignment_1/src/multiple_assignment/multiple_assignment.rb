#!/usr/bin/ruby

# *を付けると, 余った要素を配列として入れる.
a, *b = 1, 2, 3 # a = 1, b = [2, 3]
print a, ", ", b, "\n" # printでaとbを出力.

# *の付いた変数1つのみなら, 配列として全部入る.(付いてなくても配列になる.)
*a = 1, 2, 3 # a = [1, 2, 3]
print a, "\n" # printでaを出力.

# *の付いた変数に入れる要素が無い場合は, 空の配列になる.
a, *b = 1 # a = 1, b = []
print a, ", ", b, "\n" # printでaとbを出力.
