// 名前空間の登録
using System;   // 共通データ型と基本クラス(System名前空間)

// MainClassの定義
class MainClass
{
    // Mainメソッドの定義
    static void Main()
    {

        // 配列の処理化.
        char[] charArray = new char[]{'A', 'B', 'C', 'D', 'E'}; // charArrayを{'A', 'B', 'C', 'D', 'E'}で初期化.

        // charArrayの出力.
        Console.WriteLine(charArray);   // Console.WriteLineでcharArrayを出力.

    }
}